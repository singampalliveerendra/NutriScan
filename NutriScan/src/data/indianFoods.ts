export function searchIndianFoods(query: string): Product[] {
  const normalizedQuery = query.toLowerCase().trim();
  
  const matches = indianFoodsData.filter(food => 
    food.name.toLowerCase().includes(normalizedQuery) ||
    food.category.toLowerCase().includes(normalizedQuery)
  );
  
  return matches.map(food => ({
    id: `indian-${food.name.toLowerCase().replace(/\s+/g, '-')}`,
    barcode: `indian-${food.name.toLowerCase().replace(/\s+/g, '-')}`,
    name: food.name,
    brand: 'Indian Food Database',
    nutritionFacts: {
      calories: food.nutritionFacts.calories,
      protein: food.nutritionFacts.protein,
      carbohydrates: food.nutritionFacts.carbohydrates,
      fat: food.nutritionFacts.fat,
      sugar: food.nutritionFacts.sugar,
      sodium: food.nutritionFacts.sodium,
      saturatedFat: food.nutritionFacts.saturatedFat,
      fiber: food.nutritionFacts.fiber,
    },
  }));
}

